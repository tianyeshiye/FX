package ty.fx.bean;

public class TimeDataBaseBean {

	protected String timeYMDHM;
	protected String timeH;
	protected float high;
	protected float low;
	protected float open;
	protected float close;

	public String getTimeYMDHM() {
		return timeYMDHM;
	}

	public void setTimeYMDHM(String timeYMDHM) {
		this.timeYMDHM = timeYMDHM;
	}

	public String getTimeH() {
		return timeH;
	}

	public void setTimeH(String timeH) {
		this.timeH = timeH;
	}

	public float getHigh() {
		return high;
	}

	public void setHigh(float high) {
		this.high = high;
	}

	public float getLow() {
		return low;
	}

	public void setLow(float low) {
		this.low = low;
	}

	public float getOpen() {
		return open;
	}

	public void setOpen(float open) {
		this.open = open;
	}

	public float getClose() {
		return close;
	}

	public void setClose(float close) {
		this.close = close;
	}

}
